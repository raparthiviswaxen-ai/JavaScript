// methods are like functions we have to call them when we have to use the method
// A method in JavaScript is:

// A ready-made function/action that helps us perform operations on data like strings, numbers, or arrays without writing the logic from scratch.

// methods are called by "()",not properties. 

let name = "vishwa";

console.log(name.toUpperCase()); // changes to upper case
console.log(name.toLowerCase()); // changes to lower case


